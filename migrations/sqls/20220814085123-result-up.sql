CREATE TABLE IF NOT EXISTS result(
    userid INT NOT NULL DEFAULT 2 REFERENCES users(uid) on DELETE SET DEFAULT,
    quizid INT NOT NULL DEFAULT 1 REFERENCES quiz(qid) ON DELETE SET DEFAULT,
    total INT NOT NULL DEFAULT 0,
    reviewed BOOLEAN NOT NULL DEFAULT false,
    reviewed_by INT DEFAULT 1 REFERENCES users(uid) ON DELETE SET DEFAULT,
    reviewed_at DATE
)